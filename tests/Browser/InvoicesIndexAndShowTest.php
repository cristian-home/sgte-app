<?php

use App\Enums\PaymentStatus;
use App\Models\Invoice;
use App\Models\ThirdParty;
use App\Models\User;
use Illuminate\Support\Carbon;
use Laravel\Dusk\Browser;
use Spatie\Permission\Models\Role as SpatieRole;

beforeEach(function (): void {
    // migrate:fresh runs the seed_catalog_data migration, which
    // creates all 5 spatie roles (super_admin, admin, operator,
    // driver, accounting) and wires their permission grants. Tests
    // can then assign roles to factory users directly.
    $this->artisan('migrate:fresh', ['--no-interaction' => true]);
});

function invoicesAuthenticateAsSuperAdmin(): User
{
    $role = SpatieRole::firstOrCreate(['name' => 'super_admin', 'guard_name' => 'web']);
    $user = User::where('email', env('SUPER_ADMIN_USER'))->first();
    if (! $user) {
        $user = User::factory()->create([
            'email' => env('SUPER_ADMIN_USER'),
            'password' => bcrypt(env('SUPER_ADMIN_PASSWORD')),
        ]);
    }
    $user->assignRole($role);

    return $user;
}

function invoicesUserWithRole(string $roleName): User
{
    $role = SpatieRole::firstOrCreate(['name' => $roleName, 'guard_name' => 'web']);
    $user = User::factory()->create();
    $user->assignRole($role);

    return $user;
}

test('invoices index renders with Spanish headers, overdue filter, and row tint', function (): void {
    $user = invoicesAuthenticateAsSuperAdmin();

    $customer = ThirdParty::factory()->create([
        'is_natural_person' => false,
        'company_name' => 'Cliente DuskTest S.A.',
        'is_customer' => true,
    ]);

    $pending = Invoice::factory()->create([
        'third_party_id' => $customer->id,
        'invoice_number' => 'FAC-PENDING-DUSK',
        'total_value' => 1500000,
        'issue_date' => Carbon::today()->subDays(5),
        'payment_status' => PaymentStatus::Pending,
    ]);
    $overdue = Invoice::factory()->create([
        'third_party_id' => $customer->id,
        'invoice_number' => 'FAC-OVERDUE-DUSK',
        'total_value' => 2500000,
        'issue_date' => Carbon::today()->subDays(30),
        'payment_status' => PaymentStatus::Overdue,
    ]);

    $this->browse(function (Browser $browser) use ($user, $pending, $overdue): void {
        $browser->loginAs($user)
            ->visit('/invoices')
            ->waitForText('Facturas')
            ->assertSee('Número')
            ->assertSee('Cliente')
            ->assertSee('Fecha Emisión')
            ->assertSee('Valor Total')
            ->assertSee('Estado')
            ->assertSee($pending->invoice_number)
            ->assertSee($overdue->invoice_number)
            ->assertDontSee('auto-generated by Blueprint')
            ->screenshot('invoices-index-rendered');

        // Apply the overdue filter — only the expired invoice remains
        $browser->visit('/invoices?filter[payment_status]=overdue')
            ->waitForText('Facturas')
            ->assertSee($overdue->invoice_number)
            ->assertDontSee($pending->invoice_number)
            ->assertSee('Vencido')
            ->screenshot('invoices-index-overdue-filter');
    });
});

