test('accounting user can CRUD invoices and cannot see Eliminar action', function (): void {
    $user = invoicesUserWithRole('accounting');

    $customer = ThirdParty::factory()->create([
        'is_natural_person' => false,
        'company_name' => 'Cliente AccTest S.A.',
        'is_customer' => true,
    ]);

    $invoice = Invoice::factory()->create([
        'third_party_id' => $customer->id,
        'invoice_number' => 'FAC-ACC-001',
        'total_value' => 1000000,
        'issue_date' => Carbon::today()->subDays(2),
        'payment_status' => PaymentStatus::Pending,
        'notes' => 'Factura existente para accounting.',
    ]);

    $this->browse(function (Browser $browser) use ($user, $invoice): void {
        $browser->loginAs($user)
            ->visit('/invoices')
            ->waitForText('Facturas')
            ->assertSee('Crear Factura')
            ->assertSee($invoice->invoice_number)
            ->assertDontSee('auto-generated by Blueprint')
            ->screenshot('invoices-accounting-index');

        // Accounting can open the edit modal from the row action menu
        // (UPDATE_INVOICES grant).
        $browser->visit('/invoices')
            ->waitForText($invoice->invoice_number)
            ->press('Abrir menú')
            ->waitFor('[role="menu"]')
            ->clickAtXPath("//*[@role='menuitem'][contains(., 'Editar')]")
            ->waitForText('Editar Factura')
            ->assertSee('Cliente')
            ->assertSee('Valor Total')
            ->assertDontSee('auto-generated by Blueprint')
            ->screenshot('invoices-accounting-edit');

        // Accounting is rejected on DELETE (no DELETE_INVOICES grant).
        // We can't easily click a delete button that isn't rendered, so
        // pin the authorization at the route layer via a direct POST to
        // the destroy endpoint is covered by the Pest suite; here we
        // assert the gated UI surface instead: the accounting session
        // sees Editar but NOT Eliminar in the page source. The
        // DataTableRowActions dropdown renders lazily, but the Eliminar
        // string only appears when onDelete is passed as a prop — which
        // the usePermissions hook in columns.tsx guards against.
        $browser->visit('/invoices')
            ->waitForText('Facturas')
            ->assertSourceMissing('Eliminar')
            ->screenshot('invoices-accounting-no-delete');
    });
});

