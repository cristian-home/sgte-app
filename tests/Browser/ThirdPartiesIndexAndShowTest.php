test('vehicle show Ver tercero link lands on the rebuilt third-party show page', function (): void {
    $user = thirdPartiesAuthenticateAsSuperAdmin();

    $owner = ThirdParty::factory()->create([
        'is_natural_person' => false,
        'company_name' => 'Dueño del Vehículo S.A.',
        'is_customer' => false,
        'is_provider' => true,
    ]);
    $vehicle = Vehicle::factory()->create([
        'is_third_party' => true,
        'third_party_id' => $owner->id,
    ]);

    $this->browse(function (Browser $browser) use ($user, $vehicle, $owner): void {
        $browser->loginAs($user)
            ->visit("/vehicles/{$vehicle->id}")
            ->waitForText($vehicle->plate)
            ->assertSee('Ver tercero')
            ->clickLink('Ver tercero')
            ->waitForLocation("/third-parties/{$owner->id}")
            ->assertPathIs("/third-parties/{$owner->id}")
            ->assertSee($owner->company_name)
            ->assertDontSee('auto-generated by Blueprint')
            ->screenshot('vehicle-to-third-party-cross-link');
    });
});
