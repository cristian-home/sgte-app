beforeEach(function (): void {
    $this->artisan('migrate:fresh', ['--no-interaction' => true]);
});

function contractsAuthenticateAsSuperAdmin(): User
{
    $role = SpatieRole::firstOrCreate(['name' => 'super_admin', 'guard_name' => 'web']);
    $user = User::where('email', env('SUPER_ADMIN_USER'))->first();
    if (! $user) {
        $user = User::factory()->create([
            'email' => env('SUPER_ADMIN_USER'),
            'password' => bcrypt(env('SUPER_ADMIN_PASSWORD')),
        ]);
    }
    $user->assignRole($role);

    return $user;
}

test('contracts index renders the table with Spanish headers and four-state filter', function (): void {
    $user = contractsAuthenticateAsSuperAdmin();

    $customer = ThirdParty::factory()->create([
        'is_natural_person' => false,
        'company_name' => 'Transp. DuskTest S.A.',
        'is_customer' => true,
    ]);

    $vigente = Contract::factory()->create([
        'contract_number' => 'CT-VIGENTE-DUSK',
        'third_party_id' => $customer->id,
        'active' => true,
        'start_date' => Carbon::today()->subMonth(),
        'end_date' => Carbon::today()->addMonths(6),
    ]);
    $vencido = Contract::factory()->create([
        'contract_number' => 'CT-VENCIDO-DUSK',
        'third_party_id' => $customer->id,
        'active' => true,
        'start_date' => Carbon::today()->subMonths(6),
        'end_date' => Carbon::today()->subDays(10),
    ]);

    $this->browse(function (Browser $browser) use ($user, $vigente, $vencido): void {
        $browser->loginAs($user)
            ->visit('/contracts')
            ->waitForText('Contratos')
            ->assertSee('Número')
            ->assertSee('Cliente')
            ->assertSee('Objeto')
            ->assertSee('Vigencia')
            ->assertSee('Estado')
            ->assertSee($vigente->contract_number)
            ->assertSee($vencido->contract_number)
            ->assertDontSee('auto-generated by Blueprint')
            ->screenshot('contracts-index-rendered');

        // Apply the vencido filter — only the expired contract remains
        $browser->visit('/contracts?filter[contract_status]=vencido')
            ->waitForText('Contratos')
            ->assertSee($vencido->contract_number)
            ->assertDontSee($vigente->contract_number)
            ->assertSee('Vencido')
            ->screenshot('contracts-index-vencido-filter');
    });
});

