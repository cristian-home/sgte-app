test('contracts create dialog auto-generates contract_number when is_generic is true', function (): void {
    $user = contractsAuthenticateAsSuperAdmin();

    $customer = ThirdParty::factory()->create([
        'is_natural_person' => false,
        'company_name' => 'Cliente Genérico S.A.',
        'is_customer' => true,
    ]);

    $this->browse(function (Browser $browser) use ($user): void {
        $browser->loginAs($user)
            ->visit('/contracts')
            ->waitForText('Contratos')
            ->press('Crear Contrato')
            ->waitForText('Crear Contrato')
            ->assertSee('Número de Contrato')
            ->assertSee('Cliente')
            ->assertDontSee('Se generará automáticamente')
            ->screenshot('contracts-create-dialog-open');

        // Toggle the is_generic switch — verify the Input is replaced
        // with the auto-generation note.
        $browser->click('#dlg_is_generic')
            ->waitForText('Se generará automáticamente al guardar')
            ->assertSee('(GEN-####-YYYY)')
            ->screenshot('contracts-create-dialog-generic-on');
    });

    // Backend sanity check: the auto-generation logic still creates a
    // GEN-####-YYYY contract when store() is hit directly. This pins
    // the load-bearing controller logic.
    $this->actingAs($user)->post(route('contracts.store'), [
        'third_party_id' => $customer->id,
        'contract_object' => 'business',
        'start_date' => Carbon::today()->toDateString(),
        'end_date' => Carbon::today()->addYear()->toDateString(),
        'route_description' => 'Dusk generic contract.',
        'is_generic' => true,
        'active' => true,
    ]);

    $generic = Contract::query()->where('is_generic', true)->latest('id')->first();
    expect($generic)->not->toBeNull();
    expect($generic->contract_number)->toStartWith('GEN-');
});

